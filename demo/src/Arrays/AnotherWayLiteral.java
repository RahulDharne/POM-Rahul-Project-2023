package Arrays;

public class AnotherWayLiteral {
	
	   public static void main(String[] args ) {
		   
		   int [] a = {11,22,33,44,55};
		     
		   for(int i = 0;i<5 ; i++)  // i always start from 0 ,    0<5=11,1<5=22,2<5=33,3<5=44,4<5=55
			   System.out.println(a[i]);
		   
	   }

}
